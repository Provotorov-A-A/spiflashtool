//******************************************************************************
//								INCLUDES
//******************************************************************************
#include "include/common_exception.h"

//******************************************************************************
//								VARIABLES
//******************************************************************************
const std::string CommonException::common_name = (std::string)"Application";

//******************************************************************************
//								PROCEDURES
//******************************************************************************

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
CommonException::CommonException(const std::string d) : module_name(common_name), description(d) 
{
};

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
CommonException::CommonException(const std::string module, const std::string d) : module_name(module), description(d) 
{
};

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
const char* CommonException::what() const 
{
	return (module_name + " : " + description).c_str();
};

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
void CommonException::print() const
{
	if (0 == description.size())
	{
		std::cout << std::endl << "Unspecified exception occured in module \"" << module_name << "\"" << std::endl;
	}
	else
	{
		std::cout << what() << std::endl; //std::endl << "Exception occured in module \"" << module_name << "\" : "<< description << std::endl;
	}
}
